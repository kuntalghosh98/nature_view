function getCacheKey(method, path, locale) {
  return `${CACHE_PREFIX}:${CACHE_VERSION}:${locale || "en"}:${method}:${path}`;
}
function getFromCache(cacheKey) {
  // Check memory first (instant, no serialization)
  const memCached = memoryCache.get(cacheKey);
  if (memCached && memCached.expiresAt > Date.now()) {
    return memCached.data;
  }
  // Check sessionStorage (persists across navigations)
  try {
    const stored = sessionStorage.getItem(cacheKey);
    if (stored) {
      const parsed = JSON.parse(stored);
      if (parsed.expiresAt > Date.now()) {
        // Hydrate memory cache for subsequent instant access
        memoryCache.set(cacheKey, parsed);
        return parsed.data;
      }
      // Expired ÃƒÂ¢Ã¢â€šÂ¬Ã¢â‚¬Â clean up
      sessionStorage.removeItem(cacheKey);
    }
  } catch {
    // sessionStorage unavailable ÃƒÂ¢Ã¢â€šÂ¬Ã¢â‚¬Â memory cache still works
  }
  return null;
}
function setCache(cacheKey, data) {
  const entry = {
    // Store the absolute expiration timestamp
    expiresAt: Date.now() + SESSION_CACHE_TTL_MS,
    data,
  };
  memoryCache.set(cacheKey, entry);
  try {
    sessionStorage.setItem(cacheKey, JSON.stringify(entry));
  } catch {
    // sessionStorage full or unavailable ÃƒÂ¢Ã¢â€šÂ¬Ã¢â‚¬Â memory cache still works
  }
}
/**
 * Clear all cached API responses. Useful after mutations or when forcing a
 * full refresh. Bumping CACHE_VERSION also invalidates all caches.
 */
export function clearCache() {
  memoryCache.clear();
  try {
    const keysToRemove = [];
    for (let i = 0; i < sessionStorage.length; i++) {
      const key = sessionStorage.key(i);
      if (key?.startsWith(`${CACHE_PREFIX}:`)) {
        keysToRemove.push(key);
      }
    }
    keysToRemove.forEach((key) => sessionStorage.removeItem(key));
  } catch {
    // sessionStorage unavailable
  }
}

